import 'dart:io';
import 'dart:typed_data';

void main() async {
  print('Initializing assets generation...');

  final directories = [
    'assets/images',
    'assets/audio/sfx',
    'assets/audio/music',
    'assets/fonts',
  ];

  for (final dir in directories) {
    await Directory(dir).create(recursive: true);
    print('Created directory: $dir');
  }

  // 44-Byte Valid Silent WAV File Header + Data
  final wavBytes = Uint8List.fromList([
    0x52, 0x49, 0x46, 0x46, // "RIFF"
    0x24, 0x00, 0x00, 0x00, // File size - 8 (36 bytes)
    0x57, 0x41, 0x56, 0x45, // "WAVE"
    0x66, 0x6d, 0x74, 0x20, // "fmt "
    0x10, 0x00, 0x00, 0x00, // Chunk size (16)
    0x01, 0x00,             // Format (1 = PCM)
    0x01, 0x00,             // Channels (1)
    0x40, 0x1f, 0x00, 0x00, // Sample rate (8000 Hz)
    0x40, 0x1f, 0x00, 0x00, // Byte rate (8000 B/s)
    0x01, 0x00,             // Block align (1)
    0x08, 0x00,             // Bits per sample (8)
    0x64, 0x61, 0x74, 0x61, // "data"
    0x00, 0x00, 0x00, 0x00  // Data size (0)
  ]);

  final sfxFiles = [
    'sonar_ping.wav',
    'battery_pickup.wav',
    'battery_warning.wav',
    'level_complete.wav',
    'gate_open.wav',
    'collision.wav',
    'click.wav',
  ];

  for (final file in sfxFiles) {
    final path = 'assets/audio/sfx/$file';
    await File(path).writeAsBytes(wavBytes);
    print('Generated silent SFX: $path');
  }

  // A tiny silent MP3 frame bytes (approx 100 bytes of empty audio frames)
  final mp3Bytes = Uint8List.fromList([
    0xff, 0xfb, 0x10, 0xc4, 0x00, 0x03, 0xc0, 0x00, 0x01, 0xa4, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00,
    0x34, 0x80, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
  ]);

  final musicFiles = [
    'ambient_explore.mp3',
    'tension_loop.mp3',
  ];

  for (final file in musicFiles) {
    final path = 'assets/audio/music/$file';
    await File(path).writeAsBytes(mp3Bytes);
    print('Generated silent Music: $path');
  }

  // Create simple 1x1 pixel image placeholders
  final pngBytes = Uint8List.fromList([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4,
    0x89, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00,
    0x05, 0x00, 0x01, 0x0d, 0x0a, 0x2d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae,
    0x42, 0x60, 0x82
  ]);

  final imageFiles = [
    'player_orb.png',
    'battery_cell.png',
    'dna_fragment.png',
  ];

  for (final file in imageFiles) {
    final path = 'assets/images/$file';
    await File(path).writeAsBytes(pngBytes);
    print('Generated image placeholder: $path');
  }

  print('Asset generation completed successfully.');
}
